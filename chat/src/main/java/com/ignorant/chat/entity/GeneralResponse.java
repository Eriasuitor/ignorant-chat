package com.ignorant.chat.entity;

public class GeneralResponse {
	private String msg;

	public GeneralResponse() {
		super();
	}

	public GeneralResponse(String msg) {
		super();
		this.msg = msg;
	}

	public String getMsg() {
		return msg;
	}

	public void setMsg(String msg) {
		this.msg = msg;
	}
}
